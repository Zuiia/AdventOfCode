package src.Day3;

public class Claim {
    public int id;
    public int left;
    public int top;
    public int width;
    public int height;

    public Claim(int id, int left, int top, int width, int height) {
        this.id = id;
        this.left = left;
        this.top = top;
        this.width = width;
        this.height = height;
    }
}
